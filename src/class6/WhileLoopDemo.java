package class6;


public class WhileLoopDemo {
    public static void main(String[] args) {

        int number=1;
        while (number<=5){
            System.out.println(number);
            number++;
        }

        System.out.println("*****************");

        int number3=1;
        while (number3<=10){
            System.out.println(number3);
            number3+=2;
        }

    }
}

