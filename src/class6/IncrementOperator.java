package class6;


public class IncrementOperator {//+
    public static void main(String[] args) {

        int number=10;
        number=number+1;
        System.out.println(number);//11

        number+=1;
        System.out.println(number);//11
        number++; // also increment the number by 1
        System.out.println(number);//11

        int number2=10;
        number2--; // decrement the value by one
        System.out.println(number2);

    }
}

