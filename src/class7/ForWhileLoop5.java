package class7;

public class ForWhileLoop5 {
    public static void main(String[] args) {

        // print the table of 5
        for (int i = 1; i <= 10; i++) {
            System.out.println("5 * " + i + " = " + (5 * i));
        }

        System.out.println("*****************************");

        for (int n = 1; n <= 10; n++) {
            System.out.println("3 * " + n + " = " + (3 * n));
        }


    }
}
